using System;
using System.Collections.Generic;

public class aaa_integer
{
  
  
  public static void Main(String[] args)
  {
    int i = 0;
    i ++;
    Console.Write(i);
    i += 55;
    Console.Write(i);
    i *= 13;
    Console.Write(i);
    i /= 2;
    Console.Write(i);
    i ++;
    Console.Write(i);
    i /= 3;
    Console.Write(i);
    i --;
    Console.Write(i);
  }
  
}

