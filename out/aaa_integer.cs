using System;

public class aaa_integer
{
  
  
  public static void Main(String[] args)
  {
    int i = 0;
    i --;
    Console.Write(i);
    Console.Write("\n");
    i += 55;
    Console.Write(i);
    Console.Write("\n");
    i *= 13;
    Console.Write(i);
    Console.Write("\n");
    i /= 2;
    Console.Write(i);
    Console.Write("\n");
    i ++;
    Console.Write(i);
    Console.Write("\n");
    i /= 3;
    Console.Write(i);
    Console.Write("\n");
    i --;
    Console.Write(i);
    Console.Write("\n");
    /*
http://fr.wikipedia.org/wiki/Modulo_(op%C3%A9ration)#Trois_d.C3.A9finitions_de_la_fonction_modulo
*/
    int a = 117 / 17;
    Console.Write(a);
    Console.Write("\n");
    int b = 117 / -17;
    Console.Write(b);
    Console.Write("\n");
    int c = -117 / 17;
    Console.Write(c);
    Console.Write("\n");
    int d = -117 / -17;
    Console.Write(d);
    Console.Write("\n");
    int e = 117 % 17;
    Console.Write(e);
    Console.Write("\n");
    int f = 117 % -17;
    Console.Write(f);
    Console.Write("\n");
    int g = -117 % 17;
    Console.Write(g);
    Console.Write("\n");
    int h = -117 % -17;
    Console.Write(h);
    Console.Write("\n");
  }
  
}

