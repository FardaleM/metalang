import java.util.*;

public class aaa_integer
{
  static Scanner scanner = new Scanner(System.in);
  
  
  public static void main(String args[])
  {
    int i = 0;
    i --;
    System.out.printf("%d", i);
    i += 55;
    System.out.printf("%d", i);
    i *= 13;
    System.out.printf("%d", i);
    i /= 2;
    System.out.printf("%d", i);
    i ++;
    System.out.printf("%d", i);
    i /= 3;
    System.out.printf("%d", i);
    i --;
    System.out.printf("%d", i);
  }
  
}

