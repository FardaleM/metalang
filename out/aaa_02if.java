import java.util.*;

public class aaa_02if
{
  
  static boolean f(int i)
  {
    if (i == 0)
        return true;
    return false;
  }
  public static void main(String args[])
  {
    if (f(4))
        System.out.print("true <-\n ->\n");
    else
        System.out.print("false <-\n ->\n");
    System.out.print("small test end\n");
  }
  
}

