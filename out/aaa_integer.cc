#include <cstdlib>
#include <cstdio>
#include <iostream>
#include <vector>


int main(void){
  int i = 0;
  i ++;
  std::cout << i;
  i += 55;
  std::cout << i;
  i *= 13;
  std::cout << i;
  i /= 2;
  std::cout << i;
  i ++;
  std::cout << i;
  i /= 3;
  std::cout << i;
  i --;
  std::cout << i;
  return 0;
}

