import java.util.*;

public class aaa_02if
{
  
  static boolean f(int i)
  {
    if (i == 0)
      return true;
    return false;
  }
  
  
  static void main(String[] args)
  {
    if (f(4))
      print("true <-\n ->\n");
    else
      print("false <-\n ->\n");
    print("small test end\n");
  }
  
}

