using System;

public class loop_unroll
{
  
  
  public static void Main(String[] args)
  {
    int j = 0;
    j = 0;
    Console.Write(j);
    Console.Write("\n");
    j = 1;
    Console.Write(j);
    Console.Write("\n");
    j = 2;
    Console.Write(j);
    Console.Write("\n");
    j = 3;
    Console.Write(j);
    Console.Write("\n");
    j = 4;
    Console.Write(j);
    Console.Write("\n");
  }
  
}

